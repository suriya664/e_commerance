/* The Gild Store – AOS helper */

document.addEventListener("DOMContentLoaded", () => {
  if (!window.AOS) {
    console.warn("AOS library not found. Ensure aos library CDN is loaded.");
    return;
  }

  window.AOS.init({
    duration: 900,
    once: true,
    offset: 120,
    easing: "ease-out-cubic",
  });
});
